# WAP to make a simple calculator.

def addition(a, b):
    return a + b


def multiplication(a, b):
    return a * b


def division(a, b):
    return a / b


def subtraction(a, b):
    return a - b


# calling the function by given direct values
print(addition(4, 8))
print(subtraction(9, 4))
print(multiplication(4, 5))
print(division(6, 3))
