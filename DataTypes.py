# Data types in python
a = 3
print(type(a))

b: float = 7.1
print(type(b))

c = 'sid'
print(type(c))

d = True
print(type(d))

e = 0x456
print(type(e))

f = 2.0 + 3.5j
print(type(f))

g = [4, 6, 2.0, 'Sid']
print(type(g))
print(g[0])

h = {4, 6, 'rgh', 8.0}
print(type(h))

i = {1: 'Mon', 2: 'Tue', 3: 'Wed'}
print(type(i))
print(i[2])

